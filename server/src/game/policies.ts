import { PolicySlider, PolicyCategory, PolicyEffect, LinkFormula } from '../types';

// ============================================================
// POLICY DEFINITIONS â€” The core of the simulation web
// 45 policies across 9 categories, tuned for Australian politics
//
// Each policy is a slider (0-1) with interconnected effects on
// stats, voter groups, and situations. The web of effects creates
// emergent gameplay where every decision has trade-offs.
//
// Default values reflect approximate real Australian policy
// settings circa 2024. costPerPoint is billions AUD at full level.
// ============================================================

function policy(
  id: string,
  name: string,
  shortName: string,
  description: string,
  category: PolicyCategory,
  icon: string,
  minLabel: string,
  maxLabel: string,
  defaultValue: number,
  costPerPoint: number,
  implementationDelay: number,
  effects: PolicyEffect[],
  ideologicalBias: { social: number; economic: number },
): PolicySlider {
  return {
    id,
    name,
    shortName,
    description,
    category,
    icon,
    minLabel,
    maxLabel,
    currentValue: defaultValue,
    targetValue: defaultValue,
    defaultValue,
    costPerPoint,
    implementationDelay,
    effects,
    ideologicalBias,
  };
}

function effect(
  targetId: string,
  multiplier: number,
  formula: LinkFormula = 'linear',
  delay: number = 0,
  inertia: number = 0.2,
): PolicyEffect {
  return { targetId, multiplier, formula, delay, inertia };
}

